def cal_color_loss(fake_feat, color_feat):
    b, c, w, h = fake_feat.shape
    fake_feat_reshape, color_feat_reshape = fake_feat.reshape(b, -1), color_feat.reshape(b, -1)
    diff = (fake_feat_reshape - color_feat_reshape)**2
    sum_diff = torch.sum(diff, axis=-1)
    diff_norm = sum_diff / (c * w * h)
    loss_rec = torch.mean(diff_norm)

    return loss_rec

